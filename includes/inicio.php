			<p>El hombre de La Mancha (título original Man of la Mancha) es un musical de Broadway del año 1965 que cuenta la historia de Don Quijote de la Mancha como una obra de teatro dentro de una obra de teatro, representada por Miguel de Cervantes Saavedra y sus compañeros de prisión mientras espera una audiencia con la Inquisición española. Está basada en el libro del mismo nombre de Dale Wasserman, las letras de las canciones son de Joe Darion, y la música de Mitch Leigh. La canción El sueño imposible fue especialmente popular.</p>
