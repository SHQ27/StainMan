      	<h1> Pepe Cibrián </h1>
      	<div class='text-justify'>
			<p> Nació en La Habana, Cuba, el 13 de mayo de 1948, durante una gira teatral que realizaban sus padres Ana María Campoy y José Cibrián. </p>
			<p> En 1978, en plena dictadura estrena en el Teatro Embassy una de sus más famosas obras "Aquí no podemos hacerlo", considerada por el crítico del entonces prestigioso diario “La Opinión”, Daniel López, como “el primer gran musical argentino”, que reestrenará con gran éxito en 1987 en el Teatro Municipal Alvear, luego en 1994 nuevamente en el Teatro Cervantes y en 2001 en el Teatro del Globo.</p>
			<p>En 1989 recibe el premio por segunda vez de la Asociación Argentina de Autores (Argentores) uno de los mayores reconocimientos por su obra "Las Invasiones Inglesas" estrenada en el Teatro Municipal San Martín, siendo premiado como mejor autor de musicales, habiendo recibido el primero en 1981 como mejor autor de comedia por su obra “La Puritana”, y en el 2010, el de “Trayectoria”, también de Argentores, junto a Griselda Gambaro.</p>
			<p> En 1983 comienza su asociación artística con Ángel Mahler con el musical "Calígula". A partir de allí estrenarán juntos los musicales "George Sand", "Mágico Burdel", "Y al final otra vez" y "Los de la legua".</p>
			<p>Esta dupla, gracias a la confianza depositada en ellos por Tito y Ernestina Lectoure, propietarios del histórico estadio Luna Park, estrenaron en 1991: “Drácula, el musical”, el cual les valiera varios premios (ACE, Estrella de mar, Prensario, etc.) y que fuera visto por más de 3.000.000 de personas tanto en el país como en Chile ,España, Brasil, Uruguay, Paraguay y donde cumpliera sus primeros veinte años con otra más que exitosa reposición de las muchas que hicieran en todo el país. </p>
			<p>En 2012 recibió la mención de honor “Domingo Faustino Sarmiento” que entrega el Honorable Senado de la Nación (H.C.D).  Y ese mismo año estreno “Marica” la obra de teatro cuyo monólogo final sirviera como su exposición frente al Senado de la Nación, apoyando la Ley de Matrimonio Igualitario, en donde nuevamente asumirá el sol de actor. Galardonado en 2011 como “Ciudadano Ilustre” de la Ciudad Autónoma de Buenos Aires, además de la ciudad de Pilar y del partido de General Pueyrredón. </p>
			<p>Recibió el Florencio Sánchez por autor y actor por su obra Marica y Excalibur como mejor musical. </p>
			<p>Actualmente protagoniza el musical Priscilla con su personaje de Bernadette. </p>
			<p>Recibió el premio Ace junto a Angel Mahler por la trayectoria, el premio Hugo por mejor libro musical por Drácula.</p>
