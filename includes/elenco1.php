      	<img class='img-responsive cast-img' id='milone' src="img/milone.jpg">
		<h1> Cecilia Milone </h1>
		<div class='text-justify'>
			<p> Cecilia Milone nació en Buenos Aires el 7 de marzo de 1969. Cursó sus estudios primarios en el “Colegio Coronel Isidoro Suárez” y los estudios secundarios en la “Escuela Comercial Nº 4, Baldomero Fernández Moreno”, ambos establecimientos ubicados en el barrio de San Telmo, en donde se crió. A los 16 años comienza a tomar clases particulares de arte escénico con María Vaner. Al terminar la escuela secundaria ingresa al “Conservatorio Nacional de Arte Dramático”, pero abandona estos estudios apenas iniciados. Consigue entonces su primer empleo como recepcionista en un bufete jurídico y continúa tomando diferentes cursos privados de actuación con grandes profesores tales como: Carlos Gandolfo y Elena Tritek, entre otros. En el año 1987, inicia sus estudios de Bel Canto con su Maestra Susana Rossi, quien todavía la controla vocalmente.</p>
			<p> En 1991, recién cumplidos sus 22 años, es seleccionada como la protagonista femenina de la obra musical: “Drácula, el musical”, pieza escrita y dirigida por Pepe Cibrián, con música y arreglos de Ángel Mahler. Con el personaje Mina Murray, hace su debut profesional ante 5.500 espectadores el 29 de agosto de 1991, en el “Luna Park” . “Drácula, el musical” se convirtió rápidamente en un suceso. Milone participó de todas las grabaciones que se hicieron de la obra y de las giras efectuadas por el interior del país hasta 1994.</p>
			<p>En 1993 realiza su primera labor en televisión actuando en algunos capítulos de la serie “Son de diez”, ese mismo año logra su primer contrato televisivo en "Canal 9" participando en la tira: “Con pecado concebidas”. Desde entonces forma parte de varios elencos, hasta que en 1998, a partir de una incursión en el ciclo “Verdad Consecuencia”, es convocada por la productora “Pol-ka” para ser parte de: “Gasoleros”, interpretando a la doctora Isabel Panigassi. La gran repercusión que tuvo en la Argentina “Gasoleros”, significó para Milone el comienzo de su popularidad.</p>
			<p>Dentro de su labor teatral vale destacar: “Boeing Boeing”, un vodevil que contó con las actuaciones de: Rodolfo Ranni, Fabián Gianola y Ana Acosta, y por el que estuvo nominada al premio “Estrella de mar” por el personaje de Judith. “Boeing Boeing” fue un éxito, tanto en el “Teatro Neptuno” de la Ciudad de Mar del Plata como en el “Teatro Astros” de la Capital Federal. (1999-2000)</p>
			<p>Milone presentó en el año 2000 su primer trabajo discográfico como cantante solista “Besos brujos - Entre tangos y boleros”. Este disco, que produjo la compañía “Universal”, surgió a partir del espectáculo “Cecilia Milone, entre tangos y boleros”, que la actriz y cantante presentara en febrero del 99' en “La subasta”, Mar del plata; y a partir del 28 de septiembre de ese mismo año, en el “Complejo La Plaza”.</p>
			<p>Entre sus trabajos televisivos, hay que destacar “Poné a Francella”, ciclo transmitido por Telefé, que le permitió mostrarse como comediante de un modo masivo. Ese mismo año se dedicó también a la producción de su segundo disco: “Se dice de mí”.</p>
			<p>El 2003 fue un año de musicales. Desde el 9 de enero, protagonizó nuevamente “Drácula, el musical” junto a Juan Rodó en el “Teatro Ópera” y luego emprendieron una larga gira por nuestro país que se extendió durante todo el año. El 20 de diciembre fue la fecha del estreno en Mar del Plata del “Show de las divorciadas”, pieza de Manuel González Gil y Martín Bianchedi que se ofreció a partir de abril de 2004 en la sala “Pablo Neruda” del “Complejo La Plaza ”, en diversos teatros de nuestras provincias y en la ciudad de Montevideo. Paralelamente a sus trabajos televisivos y teatrales creó diversos espectáculos unipersonales. En el 2004 debutó en Capital con sus “Recuerdos surtidos”, que luego, en el 2006 presentaría en el Hotel Costa Galana durante la temporada de verano, y, durante ese mismo año realizó una gira nacional.</p>
			 <p>El verano del 2007 la encontró en el teatro “Metropolitan 2”. Protagonizó “Morocha y pasional”, un gran show de tango, con un importante cuerpo de baile, músicos en escena y una lujosa producción de Gerardo Sofovich. El espectáculo permaneció seis meses en cartel.</p>
			<p>La temporada veraniega de 2008 Cecilia estrenó “Mujeres bonitas” junto a Patricia Palmer, Laura Oliva, Natalia Lobo y Emme. Estuvo a cargo de la conducción del programa radial “Yo soy la morocha” por “Radio Brisas, 98.5 de Mar del Plata.</p>
			<p>Y ese mismo verano estrenó su primer musical: “Amores del tango”, en homenaje a Mariano Mores, formando parte de la cartelera del "Teatro Güemes" y siendo nominados al Premios “Estrella de mar”. “Amores del tango” la tuvo a Cecilia como creadora y directora y fue la obra con la cual realizó una gira nacional durante todo el año, recibió una distinción del “Honorable Concejo Deliberante del Partido de General Pueyrredón” en la Ciudad de Mar del Plata.</p>
			<p>En el 2010 fue el año elegido para el reestreno del musical que convirtió en 1991 en un gran éxito Chico Novarro "Arráncame la Vida"; y que en esta versión lo contará como director de la dupla Milone – Darthés.</p>
			<p>En septiembre del 2011 estrenó “Valiente y Sentimental” (que creó y dirigió) para el Maipo Kabaret, celebrando con música y textos sus veinte años de profesión.</p>
			<p>En diciembre del 2012 formó parte de la segunda entrega de “Primeras Damas del Musical”.</p>
			<p>Durante el 2013, después de pasar la temporada en Carlos Paz junto al Negro Álvarez, en el espectáculo "Bendito, tú eres", participó como panelista del ciclo de C5N: “Malas muchachas”, que se emitió todos los domingos a las 22hs. Ciclo, en el que, además de panelista junto a Moria Casán, Carmen Barbieri, Sofía Gala y Any Ventura la tuvo como la intérprete y creadora de la cortina musical que llevó el mismo título que el programa.</p>
			<p>Durante el verano del 2014 formó parte del elenco del musical "Sorpresas" que ofreció en Mar del Plata junto a Moria Casán, Carmen Barbieri, María Rosa Fugazot, Divina Gloria, Ivanna Rossi, y Mariana Jacazzio. Espectáculo que se estrenó en el Teatro Atlas de Mar del Plata y que contó con la dirección de Ricky Pashkus.</p>
			<p>Y condujo junto a José María Muscari la primera edición de "Señores & Señores" que se ofreció en el Teatro Gran Rex".</p>
			<p>En noviembre de este año comenzarán los ensayos de "El hombre de La Mancha", versión que tendrá su fecha de estreno en el mes de enero de 2015 en Buenos Aires, y que Milone co-protagonizará, nada menos, que junto a Pepe Cibrián y Raúl Lavié.</p>
